     public void congelaBotoes(boolean sim){
        if(sim){
            btAtacar.setEnabled(false);
            btAiroDumpMonitor.setEnabled(false);
            btRestaurar.setEnabled(false);
        }
        else{
            btAtacar.setEnabled(true);
            btAiroDumpMonitor.setEnabled(true);
            btRestaurar.setEnabled(true);
        }
     }
     
